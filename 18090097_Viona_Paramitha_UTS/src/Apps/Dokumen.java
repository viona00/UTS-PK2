    private void tbl_dokMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_dokMouseClicked
        int baris = tbl_dok.rowAtPoint(evt.getPoint());
        String nik = tbl_dok.getValueAt(baris, 1).toString();
        txt_kode.setText(nik);
        
        String nama = tbl_dok.getValueAt(baris, 2).toString();
        txt_nama.setText(nama);
        
        String jab = tbl_dok.getValueAt(baris, 3).toString();
        cmb_kategori.setSelectedItem(jab);
        
        String alamat = tbl_dok.getValueAt(baris, 4).toString();
        txt_lokasi.setText(alamat);
        
        String email = tbl_dok.getValueAt(baris, 5).toString();
        txt_desc.setText(email);
        
        String telp = tbl_dok.getValueAt(baris, 6).toString();
        txt_tgl.setText(telp);
    }//GEN-LAST:event_tbl_dokMouseClicked

